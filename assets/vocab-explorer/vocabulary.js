export function latinToCyrillic(str) {
  let s = str
    .replace(/Nj/g, "Њ")
    .replace(/nj/g, "њ")
    .replace(/Dž/g, "Џ")
    .replace(/dž/g, "џ")
    .replace(/Lj/g, "Љ")
    .replace(/lj/g, "љ");
  let out = "";
  for (const ch of s) {
    out += CYRILLIC_MAP[ch] || ch;
  }
  return out;
}

